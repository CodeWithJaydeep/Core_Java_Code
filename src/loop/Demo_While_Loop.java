package loop;

public class Demo_While_Loop {

    public static void main(String[] args) {

        // 1 to 100 Counting.......
        int i=0;
        while(i<=100){
            System.out.print(","+i);
            i++;
        }
        System.out.println();
        // 100 to 1 Counting........

        int j=100;
        while(j>=0) {
            System.out.print(","+j);
            j--;
        }
    }

    }

